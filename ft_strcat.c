#include "libft.h"
#include <stdlib.h>

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int v;

	v = 0;
	i = ft_strlen(s1);
	while (s2[v])
		s1[i++] = s2[v++];
	s1[i] = '\0';
	return (s1);
}
