#include "libft.h"

void				*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*t;

	i = 0;
	t = b;
	while (i < len)
	{
		*t = c;
		t++;
		i++;
	}
	return (b);
}
